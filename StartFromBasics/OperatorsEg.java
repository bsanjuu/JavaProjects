class OperatorsEg
{
    public static void main(String[] args)
    {
        //Operators in java
        int a = 10;
        int b = 2;

        int add=a+b;
        int sub=a-b;
        int mul=a*b;
        int div=a/b;
        int mod=a%b;
        int pow=(int)Math.pow(a,b);
        System.out.println("add :"+add+" sub :"+sub+" mul :"+mul+" div :"+div+" mod :"+mod+" pow :"+pow);
    }
}