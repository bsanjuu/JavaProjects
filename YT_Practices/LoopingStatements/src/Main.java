public class Main {
    public static void main(String[] args) {


        System.out.println("By starting from zero index");
        for(int i=0;i<5;i++)
        {
            System.out.println("For Loop "+i);
        }
        System.out.println("By starting from 1 index");

        for(int i=1;i<=5;i++)
        {
            System.out.println("For Loop "+i);
        }
        System.out.println("By decrementing from 5 index to 1");
        for(int i=5;i>0;i--)
        {
            System.out.println("For Loop "+i);
        }

    }
}