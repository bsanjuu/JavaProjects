/******************************************************************************

Program to swap two numbers

* @author Nsanj
*******************************************************************************/

public class SwapTwo
{
	public static void main(String[] args) {
		
		int a=10,b=20;
		
		System.out.print("before swap a="+a+" b= "+b);
		int t;
		t=b;
		b=a;
		a=t;
		
		System.out.println("\n after swap a="+a+" b= "+b);
		
		
		
	}
}
